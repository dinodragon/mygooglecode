#include <iostream>
using namespace std;

void main()
{
	int n=0;
	cout<<"输入十进制数：";
	cin>>n;
	cout<<"转为八进制数:"<<oct<<n<<endl;
	cout<<"转为十六进制数:"<<hex<<n<<endl;
}